package br.edu.univas.si4.BD;

public class CarrosException extends Exception {

	public CarrosException() {
		super();
	}

	public CarrosException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public CarrosException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CarrosException(String arg0) {
		super(arg0);
	}

	public CarrosException(Throwable arg0) {
		super(arg0);
	}

	
	
}
